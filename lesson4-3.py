mark= int(input("Enter Your Exam marks (0 - 100): "))
if mark >= 91:
    print("You got an A*")
elif mark >= 75:
    print("You got an A")
elif mark >= 60:
    print("You got a B")
elif mark >= 50:
    print("You got a C")
elif mark >= 35:
    print("Your got a D")
elif mark >= 20:
    print("Your got a E")
else:
    print("Your got a U")

